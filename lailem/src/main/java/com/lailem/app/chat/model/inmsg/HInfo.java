package com.lailem.app.chat.model.inmsg;

public class HInfo {
	private String id;
	private String nick;
	private String sHead;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNick() {
		return nick;
	}

	public void setNick(String nick) {
		this.nick = nick;
	}

	public String getsHead() {
		return sHead;
	}

	public void setsHead(String sHead) {
		this.sHead = sHead;
	}

}
